    [Fact]
    public void GenerateContractSet_CalledTwice_ResolvesCompleteUnduplicatedSet() {
        var source = CreateSource(
            """
            namespace Sample.Protocol {
                public interface IPacketBinding {
                }

                public sealed class PingBinding : IPacketBinding {
                }

                public sealed class AuthSessionBinding : IPacketBinding {
                }

                public static partial class PacketBindingRegistrations {
                    [Elarion.Abstractions.GenerateContractSetRegistration(typeof(IPacketBinding))]
                    public static partial Microsoft.Extensions.DependencyInjection.IServiceCollection AddPacketBindings(
                        this Microsoft.Extensions.DependencyInjection.IServiceCollection services);
                }
            }
            """);

        var assembly = EmitAndLoad(source);
        var contractType = assembly.GetType("Sample.Protocol.IPacketBinding", throwOnError: true)!;
        var registrationsType = assembly.GetType("Sample.Protocol.PacketBindingRegistrations", throwOnError: true)!;
        var method = registrationsType.GetMethod("AddPacketBindings", BindingFlags.Public | BindingFlags.Static)!;

        var services = new ServiceCollection();
        method.Invoke(null, [services]);
        method.Invoke(null, [services]);

        using var provider = services.BuildServiceProvider();
        var implementations = provider.GetServices(contractType).Select(s => s!.GetType()).ToArray();

        implementations.Should().HaveCount(2);
        implementations.Select(t => t.Name).Should().BeEquivalentTo("PingBinding", "AuthSessionBinding");
    }

    private static string CreateSource(string testSource) {
        return $$"""
                 namespace Elarion.Abstractions {
                     public enum ServiceScope {
                         Scoped = 0,
                         Singleton = 1,
                         Transient = 2
                     }

                     [System.AttributeUsage(System.AttributeTargets.Method, AllowMultiple = false, Inherited = false)]
                     public sealed class GenerateContractSetRegistrationAttribute : System.Attribute {
                         public GenerateContractSetRegistrationAttribute(System.Type contractType) {
                             ContractType = contractType;
                         }

                         public System.Type ContractType { get; }
                         public ServiceScope Scope { get; init; } = ServiceScope.Singleton;
                     }

                     [System.AttributeUsage(System.AttributeTargets.Class, AllowMultiple = false, Inherited = false)]
                     public sealed class ServiceAttribute : System.Attribute {
                         public ServiceAttribute(params System.Type[] serviceTypes) {
                         }

                         public ServiceScope Scope { get; init; } = ServiceScope.Scoped;
                     }
                 }

                 {{testSource}}
                 """;
    }

    private static string Normalize(string text) {
        return text.Replace("\r\n", "\n");
    }

    private static GeneratorDriverRunResult Generate(
        string source,
        bool assertGeneratedOutputCompiles = true) {
        var (result, _) = GenerateCore(source, assertGeneratedOutputCompiles);
        return result;
    }

    private static Assembly EmitAndLoad(string source) {
        var (_, outputCompilation) = GenerateCore(source, true);
        using var stream = new MemoryStream();
        var emitResult = outputCompilation.Emit(stream);

        emitResult.Success.Should().BeTrue();
        return Assembly.Load(stream.ToArray());
    }

    private static (GeneratorDriverRunResult Result, Compilation OutputCompilation) GenerateCore(
        string source,
        bool assertGeneratedOutputCompiles) {
        var parseOptions = new CSharpParseOptions(LanguageVersion.Preview);
        var syntaxTree = CSharpSyntaxTree.ParseText(source, parseOptions);
        var compilation = CSharpCompilation.Create(
            "ContractSetRegistrationGeneratorTests",
            [syntaxTree],
            CreateMetadataReferences(),
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        // CS8795 (partial method lacks an implementation) is expected pre-generation: the generator
        // supplies the implementation part.
        compilation.GetDiagnostics()
            .Where(diagnostic => diagnostic.Severity == DiagnosticSeverity.Error && diagnostic.Id != "CS8795")
            .Should().BeEmpty();

        var driver = CSharpGeneratorDriver
            .Create(new ContractSetRegistrationGenerator())
            .WithUpdatedParseOptions(parseOptions);
        driver = driver.RunGeneratorsAndUpdateCompilation(
            compilation,
            out var outputCompilation,
            out var generatorDiagnostics);
        var result = driver.GetRunResult();

        var unexpectedDiagnostics = generatorDiagnostics.Concat(result.Diagnostics)
            .Where(d => d.Severity == DiagnosticSeverity.Error &&
                        d.Id is not "ELSG014" and not "ELSG016" and not "ELSG017" and not "ELSG019");
        unexpectedDiagnostics.Should().BeEmpty();

        if (assertGeneratedOutputCompiles)
            outputCompilation.GetDiagnostics()
                .Where(diagnostic => diagnostic.Severity == DiagnosticSeverity.Error)
                .Should().BeEmpty();

        return (result, outputCompilation);
    }

    private static string GetGeneratedSource(GeneratorDriverRunResult result, string fileName) {
        return result.GeneratedTrees
            .Single(tree => string.Equals(Path.GetFileName(tree.FilePath), fileName, StringComparison.Ordinal))
            .GetText()
            .ToString();
    }

    private static IReadOnlyList<MetadataReference> CreateMetadataReferences() {
        var trustedPlatformAssemblies = (string?)AppContext.GetData("TRUSTED_PLATFORM_ASSEMBLIES");
        trustedPlatformAssemblies.Should().NotBeNull();

        return trustedPlatformAssemblies!
            .Split(Path.PathSeparator)
            .Select(path => MetadataReference.CreateFromFile(path))
            .ToArray();
    }
