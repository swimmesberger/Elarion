    [Fact]
    public void GenerateContractSet_TwoImplementations_EmitsGoldenSource() {
        var source = CreateSource(
            """
            namespace Sample.Protocol {
                public interface IPacketBinding {
                }

                public sealed class PingBinding : IPacketBinding {
                }

                public sealed class AuthSessionBinding : IPacketBinding {
                }

                public static partial class PacketBindingRegistrations {
                    [Elarion.Abstractions.GenerateContractSetRegistration(typeof(IPacketBinding))]
                    public static partial Microsoft.Extensions.DependencyInjection.IServiceCollection AddPacketBindings(
                        this Microsoft.Extensions.DependencyInjection.IServiceCollection services);
                }
            }
            """);

        var result = Generate(source);
        var generated = GetGeneratedSource(result, "Sample_Protocol_PacketBindingRegistrations_AddPacketBindings.g.cs");

        var expected =
            """
            // <auto-generated/>
            // Source: Elarion.Generators.ContractSetRegistrationGenerator
            #nullable enable

            using Microsoft.Extensions.DependencyInjection;
            using Microsoft.Extensions.DependencyInjection.Extensions;

            namespace Sample.Protocol;

            partial class PacketBindingRegistrations
            {
                public static partial IServiceCollection AddPacketBindings(this IServiceCollection services)
                {
                    services.TryAddEnumerable(ServiceDescriptor.Singleton<global::Sample.Protocol.IPacketBinding, global::Sample.Protocol.AuthSessionBinding>());
                    services.TryAddEnumerable(ServiceDescriptor.Singleton<global::Sample.Protocol.IPacketBinding, global::Sample.Protocol.PingBinding>());
                    return services;
                }
            }

            """;
        Normalize(generated).Should().Be(Normalize(expected));
    }

