interface GenerateRpcClientSourceOptions {
  generatedBy: string
  sourceLabel: string
  typesFileName: string
  schemasFileName: string
  methods: readonly string[]
  idempotentMethods: readonly string[]
  /** Property paths of file payloads per method ('*' = array items, [] = the value itself); omitted methods carry none. */
  paramsFilePaths: Readonly<Record<string, string[][]>>
  resultFilePaths: Readonly<Record<string, string[][]>>
}

// The framework-owned params._meta key that carries the idempotency key (matches the server's
// IdempotencyKeyNames.MetaKey).
