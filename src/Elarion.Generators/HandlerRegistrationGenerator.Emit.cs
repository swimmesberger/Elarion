namespace Elarion.Generators;

public sealed partial class HandlerRegistrationGenerator {
    private static void EmitHandlerRegistration(SourceProductionContext spc, HandlerInfo handler) {
        foreach (var diagnostic in handler.Diagnostics) spc.ReportDiagnostic(diagnostic.ToDiagnostic());

        var code = GenerateRegistration(handler);
        spc.AddSource(GenerateHandlerHintName(handler), SourceText.From(code, Encoding.UTF8));
    }

    internal static string GenerateHandlerHintName(HandlerInfo handler) {
        return $"{HintNames.Sanitize(handler.HandlerFqn)}.g.cs";
    }

    internal static string GenerateRegistration(HandlerInfo handler) {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Source: Elarion.Generators.HandlerRegistrationGenerator");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine("using Microsoft.Extensions.DependencyInjection;");
        sb.AppendLine();
        sb.AppendLine($"namespace {handler.Namespace};");
        sb.AppendLine();

        sb.AppendLine($"public static class {handler.HandlerName}Registration");
        sb.AppendLine("{");
        // The metadata field is emitted before the AppliesTo fields so an `AppliesTo(HandlerMetadata)` field
        // initializer (which references __handlerMetadata) sees it initialized — static initializers run in order.
        AppendHandlerMetadataField(sb, handler);
        AppendAppliesToFields(sb, handler);
        AppendResourceBindingsField(sb, handler);
        AppendRegistrationMethod(sb, handler);
        AppendBuildPipelineMethods(sb, handler);

        if (handler.Cacheable is not null)
            AppendCachePolicy(sb, handler);

        if (handler.CacheInvalidation is not null)
            AppendCacheInvalidationPolicy(sb, handler);

        if (handler.Idempotent is not null)
            AppendIdempotencyPolicy(sb, handler);

        sb.AppendLine("}");

        return sb.ToString();
    }

    private static void AppendAppliesToFields(StringBuilder sb, HandlerInfo handler) {
        // A decorator with a `public static bool AppliesTo(HandlerMetadata)` predicate is attached conditionally.
        // The predicate is called once here (per closed handler type, at type init) and cached; the factory below
        // reads the cached bool. The call site is a direct static invocation, so it stays AOT/trim-safe.
        for (var i = 0; i < handler.Decorators.Length; i++) {
            var decorator = handler.Decorators[i];
            if (!decorator.HasAppliesTo)
                continue;

            // The single predicate form is `AppliesTo(HandlerMetadata)`; pass the cached metadata singleton.
            sb.AppendLine($"    private static readonly bool __pipelineApplies{i} =");
            sb.AppendLine($"        {decorator.DecoratorFqn}.AppliesTo(__handlerMetadata);");
        }
    }

    private static void AppendHandlerMetadataField(StringBuilder sb, HandlerInfo handler) {
        // The resolved-pipeline cache: BuildPipeline publishes the decorators it actually attached in this
        // process here on first resolution, and HandlerMetadata.Pipeline reads it. `volatile` so the publish is
        // visible across threads (a concurrent first resolution just recomputes the identical list — benign).
        sb.AppendLine(
            "    private static volatile global::System.Collections.Generic.IReadOnlyList<global::Elarion.Abstractions.Pipeline.PipelineStep>? __pipeline;");
        sb.AppendLine();

        // The metadata singleton is always emitted now: ObservabilityDecorator (always the outermost
        // decorator) reads it to surface the pipeline, and it captures the concrete handler type so an attribute-reading decorator
        // sees the true handler regardless of its position (the inner.GetType() approach only works when innermost
        // — a fail-open footgun). The pipeline accessor is late-bound (a static lambda over the cache above), so
        // it reads empty until first resolution and the composed list after, without mutable state on the singleton.
        sb.AppendLine(
            "    private static readonly global::Elarion.Abstractions.Pipeline.HandlerMetadata __handlerMetadata =");
        sb.AppendLine(
            $"        new(typeof({handler.HandlerFqn}), typeof({handler.RequestFqn}), typeof({handler.ResponseFqn}),");
        sb.AppendLine(
            "            static () => __pipeline ?? global::System.Array.Empty<global::Elarion.Abstractions.Pipeline.PipelineStep>());");
    }

    // ServiceScope enum value → the ServiceLifetime expression the registration uses. [Handler(Scope = …)]
    // is the single source of truth for the handler's lifetime (ADR-0066); there is deliberately no
    // caller-supplied lifetime parameter — one would silently defeat the ELSG011-013 singleton verification.
    private static string LifetimeExpression(int scopeValue) {
        return scopeValue switch {
            1 => "ServiceLifetime.Singleton",
            2 => "ServiceLifetime.Transient",
            _ => "ServiceLifetime.Scoped"
        };
    }

    private static void AppendRegistrationMethod(StringBuilder sb, HandlerInfo handler) {
        sb.AppendLine($"    public static IServiceCollection Add{handler.HandlerName}(");
        sb.AppendLine("        this IServiceCollection services)");
        sb.AppendLine("    {");

        // Every handler is registered through the decorator factory because tracing is applied
        // unconditionally as the outermost decorator. The handler span is a no-op until a host
        // registers the Elarion.Handlers source, so there is no plain (undecorated) fast path.
        AppendDecoratedRegistration(sb, handler);

        sb.AppendLine("        return services;");
        sb.AppendLine("    }");
    }

    private static void AppendDecoratedRegistration(StringBuilder sb, HandlerInfo handler) {
        // The concrete handler stays unkeyed either way — BuildPipeline resolves the inner handler from it.
        // A dual unary/stream handler is emitted by two generators; first registration wins deliberately so
        // direct public registration calls cannot turn its concrete lifetime into last-registration-wins.
        sb.AppendLine($"        var __handlerLifetime = {LifetimeExpression(handler.ScopeValue)};");
        sb.AppendLine("        var __hasConcreteRegistration = false;");
        sb.AppendLine("        foreach (var descriptor in services) {");
        sb.AppendLine(
            $"            if (descriptor.IsKeyedService || descriptor.ServiceType != typeof({handler.HandlerFqn})) continue;");
        sb.AppendLine("            __handlerLifetime = descriptor.Lifetime;");
        sb.AppendLine("            __hasConcreteRegistration = true;");
        sb.AppendLine("            break;");
        sb.AppendLine("        }");
        sb.AppendLine(
            $"        if (!__hasConcreteRegistration) services.Add(new ServiceDescriptor(typeof({handler.HandlerFqn}), typeof({handler.HandlerFqn}), __handlerLifetime));");

        var interfaceType = $"global::Elarion.Abstractions.IHandler<{handler.RequestFqn}, {handler.ResponseFqn}>";
        // Normal case builds the decorator chain synchronously; a variant-dependent handler registers the
        // async-resolving proxy that awaits its variant builder on first dispatch. Either way the same shared
        // pipeline factory defines the chain.
        var factory = handler.VariantContractDeps.IsEmpty
            ? "BuildPipeline(sp)"
            : $"new global::Elarion.Abstractions.Pipeline.AsyncResolvedHandler<{handler.RequestFqn}, {handler.ResponseFqn}>(sp, BuildPipelineAsync)";

        if (handler.EventConsumerKey is null) {
            // Command/query: exactly one handler per request, resolvable typed-direct as IHandler<TReq, TResp>.
            sb.AppendLine("        services.Add(new ServiceDescriptor(");
            sb.AppendLine($"            typeof({interfaceType}),");
            sb.AppendLine($"            sp => {factory},");
            sb.AppendLine("            __handlerLifetime));");
        }
        else {
            // Event consumer (ADR-0046): keyed by the handler's identity so any number of consumers — hand-written
            // or generated actor relays — coexist for one event without colliding on the shared interface. The
            // event-subscription descriptor resolves this exact consumer via GetRequiredKeyedService(key).
            sb.AppendLine("        services.Add(new ServiceDescriptor(");
            sb.AppendLine($"            typeof({interfaceType}),");
            sb.AppendLine($"            {FormatStringLiteral(handler.EventConsumerKey)},");
            sb.AppendLine($"            (sp, __key) => {factory},");
            sb.AppendLine("            __handlerLifetime));");
        }

        if (handler.EventConsumerKey is null) {
            // The pipeline metadata singleton, resolvable by request type without building the chain: the
            // per-connection dispatch scope mode reads it to warn once when a pipeline that assumes
            // per-message scoping (transaction/idempotency) is dispatched under a reused connection scope.
            // Event consumers are excluded — many consumers share one event type, and they are not dispatched
            // through the typed connection path.
            sb.AppendLine(
                $"        services.Add(ServiceDescriptor.KeyedSingleton<global::Elarion.Abstractions.Pipeline.HandlerMetadata>(typeof({handler.RequestFqn}), __handlerMetadata));");
        }
    }

    // The shared pipeline factory: builds the decorator chain once per resolution, preserving normal scoped
    // lifetimes. The normal registration calls it synchronously; a variant handler's async builder calls
    // the same method after awaiting its variant dependencies, so the chain is defined exactly once.
    private static void AppendBuildPipelineMethods(StringBuilder sb, HandlerInfo handler) {
        var ifaceFqn = $"global::Elarion.Abstractions.IHandler<{handler.RequestFqn}, {handler.ResponseFqn}>";

        sb.AppendLine($"    private static {ifaceFqn} BuildPipeline(global::System.IServiceProvider sp)");
        sb.AppendLine("    {");
        sb.AppendLine($"                {ifaceFqn} handler =");
        sb.AppendLine($"                    sp.GetRequiredService<{handler.HandlerFqn}>();");
        // Collect the decorators actually attached, but only on the first resolution (the composition is constant
        // per process); after the cache is published every later resolution skips collection and allocates nothing.
        sb.AppendLine("                var __steps = __pipeline is null");
        sb.AppendLine(
            "                    ? new global::System.Collections.Generic.List<global::Elarion.Abstractions.Pipeline.PipelineStep>()");
        sb.AppendLine("                    : null;");

        AppendCacheDecorator(sb, handler);
        // The audit success recorder sits inside the transaction owners (the [DecoratorList] TransactionDecorator
        // and IdempotencyDecorator below), so its RecordAsync write enlists in the caller's unit of work and the
        // success record commits atomically with the handler's business writes (ADR-0045). It wraps the cache so
        // an [Auditable] cached query still records the read on a cache hit.
        AppendAuditCommitDecorator(sb, handler);
        AppendPipelineDecorators(sb, handler.Decorators);
        // Idempotency owns the unit-of-work transaction for [Idempotent] commands, so it wraps the handler and
        // the inner custom decorators — inside validation/resilience/cache-invalidation/feature-gate/authorization
        // (a denied/gated/invalid/replayed request never claims a key) and outside the handler's own work.
        AppendIdempotencyDecorator(sb, handler);
        AppendResilienceDecorator(sb, handler);
        AppendCacheInvalidationDecorator(sb, handler);
        // Validation sits just inside the feature gate (ADR-0027): an invalid request fails fast with its
        // field-keyed violations before it can touch caching, the pipeline (transaction), or the handler — but a
        // denied or feature-gated handler is never revealed through a validation error.
        AppendValidationDecorator(sb, handler);
        // The feature gate sits just inside authorization: a disabled feature never touches caching, the pipeline,
        // or the handler, but authorization still runs first so an unauthenticated caller is never told whether a
        // gated feature exists.
        AppendFeatureGateDecorator(sb, handler);
        // Authorization is the outermost functional gate (just inside tracing): a denied request never touches
        // caching, the pipeline (validation/transaction), or the handler, yet the denial is still traced.
        AppendAuthorizationDecorator(sb, handler);
        // The outer audit decorator wraps authorization so denied attempts are observed and recorded on the
        // detached path (their transaction — if any — is gone by the time a failure surfaces here). It sits
        // inside enrichment/tracing so Activity.Current supplies the record's correlation id (ADR-0045).
        AppendAuditDecorator(sb, handler);
        // Observability is emitted last, so it is the outermost decorator: its span parents any
        // cache/resilience/pipeline child spans, and its context-enrichment log scope wraps
        // authorization/validation/handler — a denied or invalid request is still attributed to its caller.
        // One decorator merges tracing + context enrichment (ADR-0059): it starts the handler span, runs the
        // built-in user-context enricher plus any host IHandlerContextEnricher (on by default across every
        // transport, inert for anonymous callers with no host enrichers), and records the execution metric.
        AppendObservabilityDecorator(sb, handler);

        // Steps were collected innermost-first (each decorator wraps the previous); reverse to execution order
        // (outermost-first) and publish once. Later resolutions found __pipeline non-null and skipped collection.
        sb.AppendLine("                if (__steps is not null) { __steps.Reverse(); __pipeline = __steps; }");
        sb.AppendLine("                return handler;");
        sb.AppendLine("    }");

        if (handler.VariantContractDeps.IsEmpty)
            return;

        // The asynchronous builder: await-resolve each variant the handler depends on into the scoped cache (so the
        // transparent unkeyed registration reads the right implementation), then build the same pipeline.
        sb.AppendLine(
            $"    private static async global::System.Threading.Tasks.ValueTask<{ifaceFqn}> BuildPipelineAsync(");
        sb.AppendLine("        global::System.IServiceProvider sp,");
        sb.AppendLine("        global::System.Threading.CancellationToken ct)");
        sb.AppendLine("    {");
        sb.AppendLine(
            "        var __variantCache = sp.GetRequiredService<global::Elarion.Abstractions.Features.VariantResolutionCache>();");
        foreach (var contractFqn in handler.VariantContractDeps.AsImmutableArray)
            sb.AppendLine($"        await __variantCache.WarmAsync<{contractFqn}>(sp, ct).ConfigureAwait(false);");

        sb.AppendLine("        return BuildPipeline(sp);");
        sb.AppendLine("    }");
    }

    private static void AppendCacheDecorator(StringBuilder sb, HandlerInfo handler) {
        if (handler.Cacheable is null)
            return;

        sb.AppendLine(
            $"                handler = new global::Elarion.Pipeline.CacheDecorator<{handler.RequestFqn}, {handler.ResponseFqn}>(");
        sb.AppendLine("                    handler,");
        sb.AppendLine(
            "                    sp.GetRequiredService<global::Elarion.Abstractions.Caching.IHandlerCache>(),");
        sb.AppendLine($"                    new {handler.HandlerName}CachePolicy());");
        AppendPipelineStep(sb, "                ", "global::Elarion.Pipeline.CacheDecorator<,>", false);
    }

    private static void AppendPipelineDecorators(StringBuilder sb, ImmutableArray<DecoratorInfo> decorators) {
        for (var i = decorators.Length - 1; i >= 0; i--) {
            var dec = decorators[i];
            // Decorators are emitted in reverse so the first configured decorator becomes the outermost wrapper.
            var indent = "                ";
            if (dec.HasAppliesTo) {
                // Attach only when the decorator's AppliesTo(HandlerMetadata) predicate returned true (cached above).
                // Braced so the pipeline-step record is added inside the same conditional as the attachment.
                sb.AppendLine($"{indent}if (__pipelineApplies{i})");
                sb.AppendLine($"{indent}{{");
                indent += "    ";
            }

            sb.Append($"{indent}handler = new {dec.DecoratorFqn}(handler");
            foreach (var dep in dec.ExtraDependencies)
                sb.Append(dep.IsHandlerMetadata
                    ? ", __handlerMetadata"
                    : $", sp.GetRequiredService<{dep.Fqn}>()");
            sb.AppendLine(");");
            // An AppliesTo custom attaches through a runtime gate, so it is a conditional pipeline step.
            AppendPipelineStep(sb, indent, dec.OpenGenericFqn, dec.HasAppliesTo);

            if (dec.HasAppliesTo)
                sb.AppendLine("                }");
        }
    }

    // Records one attached decorator into the resolved-pipeline collector (null after the first resolution).
    // `conditional` is true when attachment went through a runtime gate (AppliesTo or a soft service check).
    private static void AppendPipelineStep(StringBuilder sb, string indent, string openGenericFqn, bool conditional) {
        sb.AppendLine(
            $"{indent}__steps?.Add(new global::Elarion.Abstractions.Pipeline.PipelineStep(typeof({openGenericFqn}), {(conditional ? "true" : "false")}));");
    }

    private static void AppendResilienceDecorator(StringBuilder sb, HandlerInfo handler) {
        if (handler.ResiliencePolicyName is null)
            return;

        sb.AppendLine(
            $"                handler = new global::Elarion.Pipeline.ResilienceDecorator<{handler.RequestFqn}, {handler.ResponseFqn}>(");
        sb.AppendLine("                    handler,");
        sb.AppendLine(
            "                    sp.GetRequiredService<global::Elarion.Abstractions.Resilience.IResiliencePipelineRunner>(),");
        sb.AppendLine(
            $"                    new global::Elarion.Abstractions.Resilience.ResiliencePolicyReference {{ Name = {FormatStringLiteral(handler.ResiliencePolicyName)} }});");
        AppendPipelineStep(sb, "                ", "global::Elarion.Pipeline.ResilienceDecorator<,>", false);
    }

    private static void AppendIdempotencyDecorator(StringBuilder sb, HandlerInfo handler) {
        if (handler.Idempotent is null)
            return;

        // The synthesized inbox (Owner != null, ADR-0022) attaches softly: the delivery tiers register the
        // idempotency building blocks (AddElarionOutbox / the in-memory bus call AddElarionIdempotency), but a
        // host resolving a consumer without them — a unit test, a hand-rolled dispatcher — must degrade to the
        // un-deduped pipeline rather than fail resolution. A delivery scope also has no caller, so ICurrentUser is
        // soft-resolved (the Consumer scope never consults it). Explicit [Idempotent] keeps hard resolution: its
        // author asked for idempotency, so a missing store should fail loudly.
        var isInbox = handler.Idempotent.Owner is not null;
        var indent = "                ";
        if (isInbox) {
            sb.AppendLine(
                $"{indent}if (sp.GetService<global::Elarion.Abstractions.Idempotency.IIdempotencyStore>() is {{ }} __inboxStore)");
            sb.AppendLine($"{indent}{{");
            indent += "    ";
        }

        sb.AppendLine(
            $"{indent}handler = new global::Elarion.Pipeline.IdempotencyDecorator<{handler.RequestFqn}, {handler.ResponseFqn}>(");
        sb.AppendLine($"{indent}    handler,");
        sb.AppendLine($"{indent}    sp.GetRequiredService<global::Elarion.Abstractions.Pipeline.IUnitOfWork>(),");
        sb.AppendLine(isInbox
            ? $"{indent}    __inboxStore,"
            : $"{indent}    sp.GetRequiredService<global::Elarion.Abstractions.Idempotency.IIdempotencyStore>(),");
        sb.AppendLine(
            $"{indent}    sp.GetRequiredService<global::Elarion.Abstractions.Idempotency.IIdempotencyKeyAccessor>(),");
        sb.AppendLine($"{indent}    new {handler.HandlerName}IdempotencyPolicy(),");
        sb.AppendLine(isInbox
            ? $"{indent}    sp.GetService<global::Elarion.Abstractions.Identity.ICurrentUser>(),"
            : $"{indent}    sp.GetRequiredService<global::Elarion.Abstractions.Identity.ICurrentUser>(),");
        sb.AppendLine(
            $"{indent}    sp.GetRequiredService<global::Elarion.Abstractions.Serialization.IElarionJsonSerialization>().Options);");
        // The inbox attaches softly (runtime service check), so it is conditional; explicit [Idempotent] always attaches.
        AppendPipelineStep(sb, indent, "global::Elarion.Pipeline.IdempotencyDecorator<,>", isInbox);

        if (isInbox)
            sb.AppendLine("                }");
    }

